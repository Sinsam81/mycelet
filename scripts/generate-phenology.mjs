/**
 * Build empirical fruiting-phenology curves from the dated GBIF occurrences
 * and write them to src/lib/prediction/phenology-data.ts.
 *
 * Per species we emit a 52-week curve for the whole Nordic range ("all") plus
 * per-latitude-band curves where the sample is large enough. The scoring code
 * (species-scoring.ts) uses these instead of the hand-coded season_start/end
 * months when available, falling back gracefully otherwise.
 *
 * Run:  node --env-file=.env.local scripts/generate-phenology.mjs
 * This is climatology — re-run maybe once a year (or after a big import).
 */
import { writeFileSync } from 'node:fs';
import {
  BANDS,
  MIN_SAMPLE_ALL,
  MIN_SAMPLE_BAND,
  WEEKS,
  emptyCounts,
  finalizeCurve,
  latBand,
  weekIndexFromISO
} from './phenology-core.mjs';

const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!SUPABASE_URL || !SERVICE_KEY) {
  console.error('Mangler NEXT_PUBLIC_SUPABASE_URL / SUPABASE_SERVICE_ROLE_KEY i miljøet.');
  process.exit(1);
}
const PAGE = 1000;

async function rest(path) {
  const res = await fetch(`${SUPABASE_URL}/rest/v1/${path}`, {
    headers: { apikey: SERVICE_KEY, Authorization: `Bearer ${SERVICE_KEY}` }
  });
  if (!res.ok) throw new Error(`REST ${res.status}: ${await res.text()}`);
  return res.json();
}

async function main() {
  // Species names for a readable summary.
  const species = await rest('mushroom_species?select=id,norwegian_name&order=id');
  const nameById = new Map(species.map((s) => [s.id, s.norwegian_name]));

  // counts[speciesId] = { all: number[52], south:[], central:[], north:[] }
  const counts = new Map();
  const ensure = (sid) => {
    let c = counts.get(sid);
    if (!c) {
      c = { all: emptyCounts(), south: emptyCounts(), central: emptyCounts(), north: emptyCounts() };
      counts.set(sid, c);
    }
    return c;
  };

  let from = 0;
  let total = 0;
  for (;;) {
    const rows = await rest(
      `species_occurrences?select=species_id,latitude,observed_at&species_id=not.is.null&observed_at=not.is.null&order=id&offset=${from}&limit=${PAGE}`
    );
    if (rows.length === 0) break;
    for (const r of rows) {
      const w = weekIndexFromISO(r.observed_at);
      if (w == null) continue;
      const c = ensure(r.species_id);
      c.all[w]++;
      c[latBand(r.latitude)][w]++;
      total++;
    }
    from += rows.length;
    if (rows.length < PAGE) break;
    if (from % 25000 === 0) process.stdout.write(`  …${from} rader\r`);
  }
  console.log(`Leste ${total} daterte funn over ${counts.size} arter.`);

  const out = {};
  let bandCurves = 0;
  let allOnly = 0;
  let skipped = 0;
  for (const [sid, c] of [...counts.entries()].sort((a, b) => a[0] - b[0])) {
    const allTotal = c.all.reduce((s, v) => s + v, 0);
    if (allTotal < MIN_SAMPLE_ALL) {
      skipped++;
      continue;
    }
    const entry = { all: finalizeCurve(c.all) };
    let hasBand = false;
    for (const band of BANDS) {
      const bandTotal = c[band].reduce((s, v) => s + v, 0);
      if (bandTotal >= MIN_SAMPLE_BAND) {
        entry[band] = finalizeCurve(c[band]);
        hasBand = true;
      }
    }
    out[sid] = entry;
    if (hasBand) bandCurves++;
    else allOnly++;
  }

  console.log(
    `Skrev kurver: ${bandCurves} arter med breddegrads-bånd, ${allOnly} kun «all», ${skipped} hoppet over (<${MIN_SAMPLE_ALL} funn).`
  );

  // Pretty per-species peak week for a sanity-check summary (top 8 by volume).
  const summary = [...counts.entries()]
    .map(([sid, c]) => ({ sid, name: nameById.get(sid) ?? `#${sid}`, n: c.all.reduce((s, v) => s + v, 0), curve: out[sid]?.all }))
    .filter((s) => s.curve)
    .sort((a, b) => b.n - a.n)
    .slice(0, 8);
  console.log('\nTopp arter (peak-uke → omtrentlig måned):');
  for (const s of summary) {
    const peakWeek = s.curve.indexOf(Math.max(...s.curve));
    const month = ['jan', 'feb', 'mar', 'apr', 'mai', 'jun', 'jul', 'aug', 'sep', 'okt', 'nov', 'des'][
      Math.min(11, Math.floor((peakWeek * 7) / 30.4))
    ];
    console.log(`  ${s.name.padEnd(22)} n=${String(s.n).padStart(6)}  peak uke ${peakWeek} (~${month})`);
  }

  const header = `// AUTO-GENERATED by scripts/generate-phenology.mjs — DO NOT EDIT BY HAND.
// Empirical fruiting phenology from ${total} dated GBIF/Artsdatabanken finds.
// Each curve: ${WEEKS} weekly values in [0,1], peak week = 1.0, circular-smoothed.
// Regenerate:  node --env-file=.env.local scripts/generate-phenology.mjs

export interface SpeciesPhenology {
  all: number[];
  south?: number[];
  central?: number[];
  north?: number[];
}

export const PHENOLOGY_META = {
  d:${total}, species:${Object.keys(out).length}, weeks:${WEEKS}, minBand:${MIN_SAMPLE_BAND}, minAll:${MIN_SAMPLE_ALL}
} as const;

export const PHENOLOGY: Record<string, SpeciesPhenology> = ${JSON.stringify(out)};
`;
  const target = new URL('../src/lib/prediction/phenology-data.ts', import.meta.url);
  writeFileSync(target, header);
  console.log(`\n✓ Skrev src/lib/prediction/phenology-data.ts (${(header.length / 1024).toFixed(0)} KB)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
